/********************************************************************************************************************
*													                                                                *
*	                  P R I V A T E S     D E F I N I T I O N    O F    C O N S T A N T S                           *
*   													                                                            *
********************************************************************************************************************/

#define WATCHDOG_TIMEOUT_100MS       95

/********************************************************************************************************************
*													                                                                *
*	                                           V A R I A B L E                                                      *
*   													                                                            *
********************************************************************************************************************/
static sWDG_t    sWdtObj;

/********************************************************************************************************************
*													                                                                *
*	                          P U B L I C S  F U N C T I O N  D E C L A R A T I O N                                 *
*   													                                                            *
********************************************************************************************************************/

void vWatchdogHwInit(void)
{
	/* set watchdog period to 100ms */
	sWdtObj.u16WdtPeriod = WATCHDOG_TIMEOUT_100MS;

	/* initialization of watchdog */
	vWdtInitInst(&sWdtObj);
}


void vWatchdogRefresh(void)
{
	sWdtObj.pfvResetWdt();
}
